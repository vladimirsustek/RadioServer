/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t* rxStrBuff = NULL;
	char *pHTTPReq = NULL;
    uint32_t rxStrlng;
    uint32_t httpReqLng = 0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  APP_ShortcutUSB();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  MX_DMA_Init();
  MX_USB_DEVICE_Init();
  MX_USART3_UART_Init();
  MX_SPI1_Init();
  MX_TIM3_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */

  APP_LEDC_DisplayInit();
  APP_RDA5807M_RadioInit();
  APP_BMP280_SensorInit();
  APP_EEPROM_CheckIfOk();
  APP_ESP_InitConnect();
  APP_RTC_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	  // VCOM RX routine
      rxStrBuff = VCOMFetchReceivedLine(&rxStrlng);

      if (NULL != rxStrBuff)
      {
          /* If so and is terminated by <LF>,
             process it as command*/
          CmdDispatch(rxStrBuff, rxStrlng);
      }

      // Function returns ESP_RET_OK when HTTP request received
      if(ESP_RET_OK == ESP_CheckReceiveHTTP(&pHTTPReq, &httpReqLng))
      {
    	  // Process the request
          ESP_ProcessHTTP(pHTTPReq, httpReqLng);
      }

      // 1x per timeout (in milliseconds) check whether:
      // 				ESP is connected to WIFI
      //				RDA is functional (RSSI > 0)
      // If RDA is functional inform about freq and volm
      // And rest of the time show temperature and time
      APP_ModuleCheckStates(30*1000);

  }
  /* USER CODE END 3 */
}

